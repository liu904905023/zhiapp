     */
    public function store(StoreAnswersRequest $request,$question)
    {
        $data = [
            'user_id' => \Auth::id(),
            'body' => $request->get('body'),
            'question_id' => $question,

        ];
        $answer =  $this->answersRepository->create($data);
        $answer->question()->increment('answers_count');
        return back();
    }

